    private DefaultTableModel modelo;
    public String caminho = "src/File_pckg/Emprestimo.txt";
    public File arquivo = new File(caminho);
    public ArrayList<String> lista = new ArrayList<>();

    public void Save() 
    {
        try
        {
            try (BufferedWriter writer = new BufferedWriter(new FileWriter(arquivo,true))) 
            {
                String linha = null;
                new PrintWriter(arquivo).close();
                for(int i=0;i<lista.size();i++)
                {
                    linha = lista.get(i);
                    writer.append(linha);
                    writer.newLine();
                }
                writer.close();
            } 
        }
        catch(Exception ex)
        {
             JOptionPane.showMessageDialog(null, ex, "Erro",JOptionPane.CANCEL_OPTION);
        }
    }
    
    public void Load()
    {
        try
        {
            Scanner entrada = new Scanner(arquivo);
            while(entrada.hasNext())
            {
                String linha = entrada.nextLine();
                lista.add(linha); //Adiciona 
                String[] partes = linha.split("[|]");
                Object[] row = {partes[0],partes[1],partes[2],partes[3]};
                modelo.addRow(row);
            }
        }
        catch(IOException ex)
        {
            JOptionPane.showMessageDialog(null, "Erro: \n"+ex, 
                    "Erro de arquivo", 
                    JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }

    private void Clear() 
    {
       DescricaoTXT.setText("");
       dataChooser.setDate(null);
       EmprestadoTXT.setText("");
       ContatoTXT.setText("");
    }
    

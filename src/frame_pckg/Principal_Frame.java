    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        try
        {
            String data, desc, emprestado, contato;
            DateFormat dt = new SimpleDateFormat("dd/MM/YYYY");
            data = dt.format(dataChooser.getDate());
            desc = DescricaoTXT.getText();
            emprestado = EmprestadoTXT.getText();
            contato = ContatoTXT.getText();
            Object[] linha = {desc,emprestado,data,contato};
            String add = desc+"|"+emprestado+"|"+data+"|"+contato+"|";
            modelo.addRow(linha);
            
            lista.add(add);
            Save();
            Clear();
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex, "Erro",JOptionPane.CANCEL_OPTION);
        }
    }//GEN-LAST:event_AddBtnActionPerformed

